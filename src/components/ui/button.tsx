import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-md text-sm font-medium ring-offset-background transition-all duration-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0 font-body",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline: "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
        // ArenaX Gold Button Variants
        gold: "relative overflow-hidden group bg-gradient-to-r from-gold-dark via-gold to-gold-light text-primary-foreground font-semibold shadow-[0_0_20px_hsl(45_70%_47%/0.5)] hover:shadow-[0_0_30px_hsl(45_70%_47%/0.7)] hover:scale-105 border border-gold/30",
        goldOutline: "border-2 border-gold text-gold hover:bg-gold/10 hover:shadow-[0_0_20px_hsl(45_70%_47%/0.3)]",
        goldGhost: "text-gold hover:bg-gold/10 hover:text-gold-light",
        hero: "relative overflow-hidden group bg-gradient-to-r from-gold-dark via-gold to-gold-light text-primary-foreground font-bold text-lg shadow-[0_0_30px_hsl(45_70%_47%/0.6)] hover:shadow-[0_0_50px_hsl(45_70%_47%/0.8)] hover:scale-105 border border-gold-light/50 uppercase tracking-wider",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-11 rounded-md px-8",
        xl: "h-14 rounded-lg px-10 text-base",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "gold",
      size: "default",
    },
  },
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
  VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return (
      <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props}>
        {props.children}
        {(variant === 'gold' || variant === 'hero') && (
          <span className="absolute inset-0 -translate-x-full group-hover:animate-shimmer bg-gradient-to-r from-transparent via-white/30 to-transparent" />
        )}
      </Comp>
    );
  },
);
Button.displayName = "Button";

export { Button, buttonVariants };
